import { useEffect, useState } from 'react'
import { Session } from '@supabase/supabase-js'
import { supabase } from '../lib/supabase'
import { Player } from '../types'

const ADMIN_EMAIL = 'hagaitregerman@gmail.com'
const DEFAULT_TEAM_ID = 'aaaaaaaa-0000-0000-0000-000000000001'

export interface SessionState {
  session: Session | null
  player: Player | null
  isAdmin: boolean
  needsOnboarding: boolean
  loading: boolean
  refetchPlayer: () => void
}

function isAutoGeneratedName(player: Player, session: Session): boolean {
  const provider = session.user.app_metadata?.provider
  if (provider === 'google') return false
  if (player.full_name?.startsWith('+')) return true
  if (player.email && player.full_name === player.email.split('@')[0]) return true
  if (player.full_name === 'New Player') return true
  return false
}

export function useSession(): SessionState {
  const [session, setSession] = useState<Session | null>(null)
  const [player, setPlayer] = useState<Player | null>(null)
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    supabase.auth.getSession().then(({ data }) => {
      setSession(data.session)
      if (data.session) fetchPlayer(data.session)
      else setLoading(false)
    })

    const { data: listener } = supabase.auth.onAuthStateChange((_event, s) => {
      setSession(s)
      if (s) fetchPlayer(s)
      else { setPlayer(null); setLoading(false) }
    })

    return () => listener.subscription.unsubscribe()
  }, [])

  async function fetchPlayer(s: Session) {
    const uid = s.user.id

    let { data } = await supabase
      .from('players')
      .select('*')
      .eq('player_id', uid)
      .single()

    // Fallback: trigger may have failed — create the row here
    if (!data) {
      const u = s.user
      const fullName =
        u.user_metadata?.full_name ??
        u.user_metadata?.name ??
        (u.email ? u.email.split('@')[0] : null) ??
        u.phone ??
        'New Player'

      await supabase.from('players').upsert(
        {
          player_id: uid,
          team_id: DEFAULT_TEAM_ID,
          full_name: fullName,
          email: u.email ?? null,
          phone: u.phone ?? null,
          role: u.email === ADMIN_EMAIL ? 'admin' : 'player',
        },
        { onConflict: 'player_id' },
      )

      const { data: created } = await supabase
        .from('players')
        .select('*')
        .eq('player_id', uid)
        .single()
      data = created
    }

    setPlayer(data ?? null)
    setLoading(false)
  }

  const needsOnboarding =
    !!session && !!player && isAutoGeneratedName(player, session)

  return {
    session,
    player,
    isAdmin: session?.user?.email === ADMIN_EMAIL,
    needsOnboarding,
    loading,
    refetchPlayer: () => { if (session) fetchPlayer(session) },
  }
}
